# todo sobre la función print

# imprimir texto
print("Hola Jonathan")

# formatos de print
print("hacer salto de linea -> \nprueba.")
print("hacer tab (tabulación) -> \tprueba.")

# cancelar formato
print("hacer salto de linea -> \\nprueba.")
print("hacer tab (tabulación) -> \\tprueba.")

# varias líneas
print("""un texto de varias líneas 
usando tres comillas.""")

